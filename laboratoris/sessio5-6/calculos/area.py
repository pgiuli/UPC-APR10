PI = 3.1416
def area_cuadrado(lado):
    """ Calcula el area de un cuadrado
    Input: Lado en cm
    Return: Área en cm2
    """
    return lado ** 2
def area_rectangulo(base, altura):
    """ Calcula el area de un rectangulo
    Input: Base en cm y Altura en cm
    Return: Área en cm2
    """
    return base*altura
def area_circulo( radio ):
    """Calcula el area del circulo
    Input: rado en m
    Return área en cm2
    """
    return PI*radio** 2